public class If_ELse_8 {
    public static void main(String[] args) {

        /*
         The condition is the boolean literal `true`.

         Since the condition is ALWAYS true,
         the statement inside the if block
         will ALWAYS execute.

         Curly braces are not required because
         only ONE statement is present.
        */
        if (true)
            System.out.println("Hello");
    }
}
