public class If_Else_9 {
    public static void main(String []args) {

        /*
         This is a VALID Java statement.

         Explanation:
         ------------
         The semicolon (;) after `if (true)` represents an EMPTY STATEMENT.

         So this line means:
         if (true)
             do nothing;

         There is NO body associated with the if-condition.
         Hence, nothing is executed even though the condition is true.

         This is NOT a compile-time error,
         but it is usually a LOGICAL MISTAKE.
        */
        if (true);
    }
}
