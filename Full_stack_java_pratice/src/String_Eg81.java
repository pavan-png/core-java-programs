public class String_Eg81 {
    public static void main(String[] args) {

        // Creating a String variable
        String s = "H";

        // repeat(int count)
        // This method repeats the given string 'count' number of times
        // It is available from Java (JDK) 11 onwards
        // Here, "H" is repeated 5 times
        // Output: HHHHH
        System.out.println(s.repeat(5));
    }
}
