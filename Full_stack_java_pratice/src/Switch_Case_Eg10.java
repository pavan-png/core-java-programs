public class Switch_Case_Eg10 {
    public static void main(String[] args) {
        // int x = 0;
        // int x = 1;
         // int x = 2;
        int x = 0;
        switch (x){
            default : System.out.println("default");
            case 0:
                System.out.println("0");
                break;
            case 1:
                System.out.println("1");
            case 2:
                System.out.println("2");
        }
    }
}
