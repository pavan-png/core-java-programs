public class Switch_Eg15 {
    public static void main(String[] args) {
        String fruit = "apple";
        switch (fruit) {
            default:
                System.out.println("Any fruit will do");
            case "Apple":
                System.out.println("Apple");
            case "mango":
                System.out.println("Mango");
            case "Banana":
                System.out.println("Banana");
                break;


        }
    }
}
