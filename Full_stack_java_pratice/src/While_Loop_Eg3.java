public class While_Loop_Eg3 {
    public static void main(String[] args)
    {
        // while loop with condition 'true'
        // The semicolon (;) here means the loop body is EMPTY
        // This creates an infinite loop that does nothing

        while (true);

        // This line is NEVER reached
        // because the while loop above never terminates
        // and there is no break statement
    }
}
